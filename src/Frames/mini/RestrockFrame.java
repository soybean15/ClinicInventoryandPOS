    private void updateStocks(String value){
        String sql = "Update tbl_iteminfo set item_stock ='"+value+"' where item_code ='"+id+"'";
        try{
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.execute();
        }catch(Exception e ){
            System.out.println("error update "+e);
        }
    }
    
    private void insertStocks(String qty, String date, String batch_code, String item_code){
        System.out.println(batch_code.length());
        String sql = "insert into tbl_itemstock(qty, date, batch_code, item_code) values(?,?,?,?)";
        try{
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, qty);
            pst.setString(2, date);
            pst.setString(3, batch_code);
            pst.setString(4, item_code);
            pst.execute();
        }catch(Exception e ){
            System.out.println("error update "+e);
        }
    }
    
    
    
    
 

