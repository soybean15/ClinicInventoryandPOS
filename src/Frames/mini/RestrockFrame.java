    private void panelAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_panelAddMouseClicked
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("MM/dd/YYYY");  
        DateTimeFormatter dtf2 = DateTimeFormatter.ofPattern("MMdd-YY");  
         
        LocalDateTime now = LocalDateTime.now();
        String dateCode = dtf2.format(now);
        System.out.println(dateCode);
        String batch_code = generateBatchCode(dateCode);
        String date = dtf.format(now);
       // System.out.println(dtf.format(now));  
        String qty = txtStocks.getText();
        
        
        
        
        int reply = JOptionPane.showConfirmDialog(null, "Adding "+qty+" to "+name, title, JOptionPane.YES_NO_OPTION);
        if (reply == JOptionPane.YES_OPTION) {
             updateStocks(lblTotal.getText());
             insertStocks(qty, date, batch_code, id);
             JOptionPane.showMessageDialog(null, qty+"pcs of "+name+" was Added to Inventory");
            log.insertToLog(user.getUserName(), "Added "+qty+"pcs of stock on "+name);
           
          //  i.hide();
            i.displayAllItem("Select * from tbl_items inner join tbl_iteminfo on tbl_items.item_code = tbl_iteminfo.item_code");
           
             dispose();
        }
    }//GEN-LAST:event_panelAddMouseClicked

