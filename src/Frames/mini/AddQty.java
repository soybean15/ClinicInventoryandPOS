    private void addOnCart(){
        if (txtQty.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Please enter qty");
        } else {
            if (Integer.parseInt(txtQty.getText()) == 0) {
                JOptionPane.showMessageDialog(null, "Please add qty");
            } else {
                
               // item.setQty(txtQty.getText());
                
                int _stock = Integer.parseInt(item.getItemStock());
                int qty = Integer.parseInt(txtQty.getText());
                
                int newQty = _stock - qty;
                item.setStock(String.valueOf(newQty));
                if(PointOfSales.cartItem.contains(item)){
                    System.out.println(PointOfSales.cartItem.contains(item));
                   int oldQty =Integer.parseInt(item.getQty());
                    System.out.println("old"+oldQty);
                   int updatedQty = oldQty+qty;
                   item.setQty(String.valueOf(updatedQty));
                }else{
                     item.setQty(txtQty.getText());
                     PointOfSales.cartItem.add(item);
                }
               
                pos.loadCart();
                pos.loadDefault();
                dispose();
            }
        }
    }

