        //str = str.replaceAll("[^\\d]", "");
        str = str.replaceAll("[^0-9]", "");

        try {
            if (Integer.parseInt(str) > qty) {
                txtQty.setText(String.valueOf(qty));
            } else {
                txtQty.setText(String.valueOf(str));
            }
        } catch (Exception e) {
            System.out.println(e);
        }


